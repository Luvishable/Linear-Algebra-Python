# LINEAR INDEPENDENCE

# All possible linear combinations of two vectors can form a plane only if those two vectors do not lie on the same line.

# A set of M vectors is independent if each vector points in a geometric dimension not reachable using other vectors
# in the set.

# Dependence or independence are terms that describe a set of vectors, not any individual vector within that set.

# If there are more vectors than dimensions that means that the set is linearly dependent.

# The formal definition of linear dependence is related to determining whether a weighted combination of the vectors
# in the set can form the zero vector and importantly not all scalars can be equal to zero.